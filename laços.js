/**
 * Trabalhando com laços/estruturas de repetição em JavaScript
 * Exemplos com laço/estrutura de repetição "while"
 */


let numero = 3;
let contador = 0;

console.log("Tabuada de multiplicação do Nº " + contador);

while (contador<=10) {
    let resultado = numero * contador;

    console.log(numero + " x " + contador + " = " + resultado);

    contador++;
}